/// <summary>
/// Represents the status of a game.
/// </summary>
public enum GameStatus
{
    /// <summary>
    /// The game has been won.
    /// </summary>
    Win,

    /// <summary>
    /// The game has been lost.
    /// </summary>
    Lose,

    /// <summary>
    /// The game is currently idle.
    /// </summary>
    Idle
}